# 1. Crear SCHEMA
CREATE SCHEMA remesas;

USE remesas;

#2. Crear tablas
#2.1 Crear tabla de Cliente
CREATE TABLE CLIENTE (
    id_cliente INT PRIMARY KEY AUTO_INCREMENT NOT NULL,
    nombre VARCHAR(100),
    apellido_paterno VARCHAR(100),
    apellido_materno VARCHAR(100),
    fecha_nacimiento DATE,
    telefono VARCHAR(20),
    email VARCHAR(150),
    fecha_registro DATETIME
);

#2.2 Crear tabla de Beneficiario
CREATE TABLE BENEFICIARIO (
    id_beneficiario INT PRIMARY KEY AUTO_INCREMENT NOT NULL,
    nombre VARCHAR(100),
    apellido_paterno VARCHAR(100),
    apellido_materno VARCHAR(100),
    telefono VARCHAR(20),
    email VARCHAR(150)
);

#2.3 Crear tabla de Agente
CREATE TABLE AGENTE (
    id_agente INT PRIMARY KEY AUTO_INCREMENT NOT NULL,
    nombre_agente VARCHAR(150),
    pais VARCHAR(100),
    ciudad VARCHAR(100),
    direccion VARCHAR(200)
);

#2.4 Crear tabla de Tasa de Cambio
CREATE TABLE TASA_CAMBIO (
    id_tasa_cambio INT PRIMARY KEY NOT NULL,
    moneda_origen VARCHAR(3),
    moneda_destino VARCHAR(3),
    fecha DATE,
    tipo_cambio DECIMAL(18,6)
);

#2.5 Crear tabla de Transacción (tabla de Hechos)
CREATE TABLE TRANSACCION (
    id_transaccion INT PRIMARY KEY AUTO_INCREMENT NOT NULL,
    id_cliente INT,
    id_beneficiario INT,
    id_agente_origen INT,
    id_tasa_cambio INT,
    fecha_creacion DATETIME,
    monto_origen DECIMAL(10,2),
    monto_destino DECIMAL(14,2),
    estatus VARCHAR(20),
    FOREIGN KEY (id_cliente) REFERENCES CLIENTE(id_cliente),
    FOREIGN KEY (id_beneficiario) REFERENCES BENEFICIARIO(id_beneficiario),
    FOREIGN KEY (id_agente_origen) REFERENCES AGENTE(id_agente),
    FOREIGN KEY (id_tasa_cambio) REFERENCES TASA_CAMBIO(id_tasa_cambio)
);

#2.6 Crear tabla de Transacción_comisión
CREATE TABLE TRANSACCION_COMISION (
    id_comision INT PRIMARY KEY,
    id_transaccion INT,
    porcentaje_comision DECIMAL(5,2),
    monto_comision DECIMAL(10,2),
    fecha_aplicacion DATETIME,
    FOREIGN KEY (id_transaccion) REFERENCES TRANSACCION(id_transaccion)
);

#2.7 Crear tabla de Cancelación
CREATE TABLE CANCELACION (
    id_cancelacion INT PRIMARY KEY,
    id_transaccion INT,
    fecha_cancelacion DATETIME,
    motivo_cancelacion VARCHAR(200),
    reembolsado BIT,
    FOREIGN KEY (id_transaccion) REFERENCES TRANSACCION(id_transaccion)
);

#2.8 Crear tabla de Reembolso
CREATE TABLE REEMBOLSO (
    id_reembolso INT PRIMARY KEY,
    id_cancelacion INT,
    id_transaccion INT,
    fecha_reembolso DATETIME,
    monto_reembolso DECIMAL(10,2),
    metodo_reembolso VARCHAR(50),
    FOREIGN KEY (id_cancelacion) REFERENCES CANCELACION(id_cancelacion),
    FOREIGN KEY (id_transaccion) REFERENCES TRANSACCION(id_transaccion)
);

